/// Factory calibration parameters extracted from Libre sensor FRAM.
/// These parameters are read via NFC during sensor activation.
public struct LibreCalibrationInfo: Sendable, Codable, Equatable {
    /// Table index (1-based) for t1/t2 lookup
    public let i2: Int
    
    /// Lower raw glucose reference point
    public let i3: Double
    
    /// Upper raw glucose reference point
    public let i4: Double
    
    /// Temperature adjustment constant
    public let i6: Double
    
    /// User-applied slope correction (default: 1.0)
    public var extraSlope: Double
    
    /// User-applied offset correction in mg/dL (default: 0.0)
    public var extraOffset: Double
    
    /// Footer CRC for validation
    public let footerCRC: Int
    
    public init(
        i2: Int,
        i3: Double,
        i4: Double,
        i6: Double,
        extraSlope: Double = 1.0,
        extraOffset: Double = 0.0,
        footerCRC: Int = 0
    ) {
        self.i2 = i2
        self.i3 = i3
        self.i4 = i4
        self.i6 = i6
        self.extraSlope = extraSlope
        self.extraOffset = extraOffset
        self.footerCRC = footerCRC
    }
    
    /// Extract calibration info from FRAM bytes (344 bytes)
    /// Source: LibreTransmitter/SensorData.swift lines 240-249
    public static func extract(from fram: [UInt8]) -> LibreCalibrationInfo? {
        guard fram.count >= 344 else { return nil }
        
        _ = readBits(fram, 2, 0, 3)  // i1 reserved for protocol parsing
        let i2 = readBits(fram, 2, 3, 10)
        let i3 = Double(readBits(fram, 0x150, 0, 8))
        let i4 = Double(readBits(fram, 0x150, 8, 14))
        let negativei3 = readBits(fram, 0x150, 0x21, 1) != 0
        _ = Double(readBits(fram, 0x150, 0x28, 12) << 2)  // i5 reserved for protocol parsing
        let i6 = Double(readBits(fram, 0x150, 0x34, 12) << 2)
        
        // Calculate footer CRC
        let footer = Array(fram[320..<344])
        let footerCRC = Int(crc16(Array(footer.dropFirst(2))))
        
        return LibreCalibrationInfo(
            i2: i2,
            i3: negativei3 ? -i3 : i3,
            i4: i4,
            i6: i6,
            footerCRC: footerCRC
        )
    }
    
    /// Read bits from buffer (LibreTransmitter compatible)
    private static func readBits(_ buffer: [UInt8], _ byteOffset: Int, _ bitOffset: Int, _ bitCount: Int) -> Int {
        guard bitCount > 0 else { return 0 }
        var result = 0
        for i in 0..<bitCount {
            let totalBitOffset = byteOffset * 8 + bitOffset + i
            let abyte = totalBitOffset / 8
            let abit = totalBitOffset % 8
            guard abyte < buffer.count else { continue }
            if (buffer[abyte] >> abit) & 0x1 == 1 {
                result |= (1 << i)
            }
        }
        return result
    }
    
    /// CRC-16 CCITT
    private static func crc16(_ data: [UInt8], seed: UInt16 = 0xFFFF) -> UInt16 {
        var crc = seed
        for byte in data {
            crc ^= UInt16(byte) << 8
            for _ in 0..<8 {
                if crc & 0x8000 != 0 {
                    crc = (crc << 1) ^ 0x1021
                } else {
                    crc <<= 1
                }
            }
        }
        return crc
    }
}

// MARK: - Raw Measurement

/// Raw measurement values from Libre sensor
public struct LibreRawMeasurement: Sendable {
    /// Raw glucose value (14 bits)
    public let rawGlucose: Int
    
    /// Raw temperature value (12 bits, left-shifted 2)
    public let rawTemperature: Int
    
    /// Raw temperature adjustment (9 bits with sign)
    public let rawTemperatureAdjustment: Int
    
    public init(rawGlucose: Int, rawTemperature: Int, rawTemperatureAdjustment: Int) {
        self.rawGlucose = rawGlucose
        self.rawTemperature = rawTemperature
        self.rawTemperatureAdjustment = rawTemperatureAdjustment
    }
    
    /// Extract raw measurement from 6-byte block
    /// Source: LibreTransmitter/Measurement.swift lines 101-109
    public static func extract(from bytes: [UInt8]) -> LibreRawMeasurement? {
        guard bytes.count >= 6 else { return nil }
        
        // Raw glucose: 14 bits at byte 0, bit 0
        let rawGlucose = readBits(bytes, 0, 0, 14)
        
        // Raw temperature: 12 bits at bit 26, left shift 2
        let rawTemperature = readBits(bytes, 0, 26, 12) << 2
        
        // Temperature adjustment: 9 bits at bit 38, with sign bit at 47
        let temperatureAdjustment = readBits(bytes, 0, 38, 9) << 2
        let negativeAdjustment = readBits(bytes, 0, 47, 1) != 0
        let rawTemperatureAdjustment = negativeAdjustment ? -temperatureAdjustment : temperatureAdjustment
        
        return LibreRawMeasurement(
            rawGlucose: rawGlucose,
            rawTemperature: rawTemperature,
            rawTemperatureAdjustment: rawTemperatureAdjustment
        )
    }
    
    private static func readBits(_ buffer: [UInt8], _ byteOffset: Int, _ bitOffset: Int, _ bitCount: Int) -> Int {
        guard bitCount > 0 else { return 0 }
        var result = 0
        for i in 0..<bitCount {
            let totalBitOffset = byteOffset * 8 + bitOffset + i
            let abyte = totalBitOffset / 8
            let abit = totalBitOffset % 8
            guard abyte < buffer.count else { continue }
            if (buffer[abyte] >> abit) & 0x1 == 1 {
                result |= (1 << i)
            }
        }
        return result
    }
}

// MARK: - Glucose Calculation

/// Libre glucose calibration algorithm
/// Source: LibreTransmitter/GlucoseFromRaw.swift lines 271-327
public enum LibreGlucoseCalculator {
    
    /// Convert raw glucose to calibrated mg/dL value
    /// Uses factory calibration with temperature compensation (Steinhart-Hart equation)
    public static func glucoseValueFromRaw(
        measurement: LibreRawMeasurement,
        calibration: LibreCalibrationInfo
    ) -> Double {
        // Constants for temperature calculation
        let x: Double = 72500  // 1000 + 71500
        let y: Double = 1000
        
        // Steinhart-Hart coefficients for thermistor
        let ca = 0.0009180023
        let cb = 0.0001964561
        let cc = 0.0000007061775
        let cd = 0.00000005283566
        
        let rawTemperature = Double(measurement.rawTemperature)
        let rawTemperatureAdjustment = Double(measurement.rawTemperatureAdjustment)
        let rawGlucose = Double(measurement.rawGlucose)
        
        // Step 1: Calculate resistance from temperature sensor
        let rLeft = rawTemperature * x
        let rRight = rawTemperatureAdjustment + calibration.i6
        
        guard rRight != 0 else { return 0 }
        let R = (rLeft / rRight) - y
        
        guard R > 0 else { return 0 }
        
        // Step 2: Calculate temperature using Steinhart-Hart equation
        let logR = log(R)
        let d = pow(logR, 3) * cd + pow(logR, 2) * cc + logR * cb + ca
        
        guard d != 0 else { return 0 }
        let temperature = 1 / d - 273.15  // Convert Kelvin to Celsius
        
        // Step 3: Normalize raw glucose using factory calibration
        let glucoseRange = calibration.i4 - calibration.i3
        guard glucoseRange != 0 else { return 0 }
        let g1 = 65.0 * (rawGlucose - calibration.i3) / glucoseRange
        
        // Step 4: Apply temperature compensation
        let g2 = pow(1.045, 32.5 - temperature)
        let g3 = g1 * g2
        
        // Step 5: Look up correction factors from tables
        // Bound to minimum of both array sizes to prevent index out of range
        let maxIndex = min(LibreCalibrationTables.t1.count, LibreCalibrationTables.t2.count) - 1
        let tableIndex = max(0, min(calibration.i2 - 1, maxIndex))
        let v1 = LibreCalibrationTables.t1[tableIndex]
        let v2 = LibreCalibrationTables.t2[tableIndex]
        
        guard v2 != 0 else { return 0 }
        
        // Step 6: Final calibrated glucose
        let glucose = (g3 - v1) / v2
        
        // Step 7: Apply user calibration adjustments
        return glucose * calibration.extraSlope + calibration.extraOffset
    }
    
    /// Simple fallback calibration (when factory calibration unavailable)
    /// This is less accurate but provides approximate values
    public static func glucoseValueSimple(rawGlucose: Int) -> Double {
        Double(rawGlucose) / 8.5
    }
}

